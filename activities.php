<h1>Activities</h1>

<p>Counselors and teachers work hard to provide activities that cater to that day’s important theme and message, challenge the girls to expand their perspectives, and let the campers have fun. Listed below are just a few of the activities that we do to help campers expand their perspectives and achieve their goals.</p>

<h3>Senegalese Pow-wow</h3>
<p>During this session the girls will discuss various aspects they like and don’t like about Senegalese culture.  It is designed to help them think critically about their culture and how their culture affects their lives.  To encourage participation by all the girls, we will break the girls up into small groups, each of which will be facilitated by one of the teachers.  Once the girls have created a list of things they like and don’t like, they will present that information to the whole group.  After these presentations, the girls will have time to discuss as a large group what each group presented and the significance of acknowledging the influence their culture has on their lives.</p>

<h3>3 Food Groups and Vitamins</h3>
<p>At the beginning of this session we will present information to the girls about the three major food groups (“go” foods, i.e. carbohydrates; “grow” foods, i.e. protein; and “glow” foods, i.e. fruits and vegetables) and the importance of eating the right balance of them.  After that, the girls will break up into small groups, with a teacher as a facilitator for each group.  The girls will discuss what foods they eat and when (since it probably varies throughout the year), if they miss any food groups and when, and also how they can help improve their nutrition.  Then the girls will present a brief summary of their discussion to the whole group, and the girls will discuss what everyone presented.</p>

<h3>Fitness Session</h3>
<p><img src="photos/kaolackcamp_002_200.jpg" class="intext_left" width="200" />We will briefly present information about fitness (ex. the different “types” of fitness: cardiovascular, strength, flexibility), the importance of staying fit, and various ways to stay fit.  Then the girls will break up into small groups and talk about fitness, what they learn and do in PE class, what types of fitness activities they do at home, what types of fitness they’re lacking in, and what they can do to improve their fitness.  Then each group will present a brief summary of their discussion to the whole group, and the girls will discuss what everyone presented.</p>

<h3>Spa Night</h3>
<p>Doing a yoga and spa treatment session with the girls is empowering in several ways! Yoga is a total health care system, it promoting flexibility, cardio vascular and strength building, and stress relief. It is important to teach girls to be good to themselves and nurture their bodies. Yoga and spa treatments are the epitome of self nurturance. Feeling good inside and out is one of the best ways to boost self-confidence.</p>

<p>Simple spa treatments, without spending a lot of money, are easy. Foot soaks are a great and easy option. We will make salt or sugar body scrubs to use for foot and hand treatments and with lotion, the session can end with feet and hand massages. Body treatments are a great gift to give oneself and others. The valuable notion of reciprocation is well learned through exchanging treatments as well. It is our hope to expose the girls to these ideas, and encourage them to be good to their bodies and share that knowledge with other women in their lives. At the end of the session, each girl goes home with a modest spa goodie bag. Items include be soap (which ties in well to personal hygiene), lotion, loofahs, menstrual pads, small satchels of body scrub.</p>

<h3>Complete The Story</h3>
<p>We will read a story about a girl’s life to the campers. The story will end when the girl reaches the 5e class, which is the average level of the campers. Then we will break the girls into groups and each group will finish the story assuming different situations. For example, one group may be given the task of finishing the girl’s story assuming she was to become pregnant and forced to end her education, while  another option could be to assume the girl was to remain in school and complete  her education at the university level. We can break the girls into five groups, each one facilitated by one of the five teachers. Afterwards, the girls return to the main group and share and discuss their stories.</p>


