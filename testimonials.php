<h1>Testimonials</h1>

<h3>Sarah Sprague (Environmental Education Volunteer)</h3>
<p><img src="photos/sprague_sarah_150.jpg" class="intext_left" width="150" />As a new volunteer in Senegal, I was able to participate the Camp de Croissance and Croissance only weeks after arriving. Though I had not spent much time in the country yet, it was immediately evident how powerful this experience was for the girls involved. Women in Senegal are generally expected to follow a prescribed life path in which they go to school until finding a husband. They may be married as the second or third wife, and are expected to have children and work at the home. It has been said that the development of a country may be measured by the success of its women. I believe this is true, and that it is vital for young women to understand their options in life, as well as be rewarded for academic success and achievements. CCC is an amazing opportunity for girls to develop a sense of self worth, efficacy, and most importantly, lifelong friendships with other girls of the same age.</p>
<p><img src="photos/kaolackcamp_005_200.jpg" class="intext_right" width="200" />My role at the camp was to work as a lifeguard, something which I had done at girls’ camps back in the US. My local language skills were still developing at that point, so the extent of my involvement was to brokenly explain swimming and then attempt to mime it out on dry land, much to their amusement. I doubt any of them felt confident that the mumbling toubab (white person) could possibly help them in an emergency. However, we were only in 4 feet of water, so they generally decided to trust me. Being a muslim country, Senegalese culture requires most women to be covered from their waist to their ankles. Most of them had never been in the water before, much less had the opportunity to wear a bathing suit. Nevertheless, they were all enthusiastic to put on whatever bathing clothes they had found (everything from spandex jumpsuits to sparkly leotards) and get in the water. Throughout the week, I watched the girls transform from a group of enthusiastic, and sometimes nervous collection of individuals who had no idea how to swim, into to a cohesive community, playing water games, splashing around, laughing, and helping each other out. Soon, girls were coming up to me during any free time and begging to swim just for a few minutes.</p>
<p>Women in Senegal are possibly the most resilient and hardworking people I have ever encountered. Giving young women the chance to direct their efforts toward the development of their own country, and the realization of their individual dreams, is an experience I wouldn’t trade for anything. The future of this country lies with its women, and their ability to stand up and live the life they hope for. Camp de Croissance and Croissance is the perfect opportunity to provide them with the internal resources to take hold of their own lives, and choose a direction that is right for each of them; something that would not be possible without the support of other participants, local volunteers, donors, and Peace Corps. That said, I am looking forward to another amazing week of camp in June, 2011!</p>
     
     
     
     
     
     
