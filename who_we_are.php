<h1>Who We Are</h1>

<h3>The Camp</h3>
<p><img src="photos/kaolackcamp_004_200.jpg" class="intext_right" width="200" />Camp de Connaissance et Croissance is a weeklong, non-profit leadership camp designed and facilitated by Peace Corps Senegal Volunteers. Our goal is to provide young girls of Senegal with a unique opportunity to foster creativity, teamwork, and leadership. Each camp day is jam-packed with fun and exciting activities that encourage and inspire. Girls will participate in discussions, physical activities such as sports games, arts and crafts, music, and leadership building activities.</p>

<h3>The Campers</h3>
<p>Camp de Connaissance et Croissance invites forty girls from across the Kaolack, Fatick, and Kaffrine regions of Senegal to become campers.  Peace Corps Senegal Volunteers, in conjunction with local schools select girls with the top academic performance from the 5ieme level (15-16 years old) who have active and participative personalities. The selection process begins months in advance and consists of interviews and discussion with families and teachers.</p>

<h3>The Counselors - Peace Corps Senegal Volunteers</h3>
<p>The Peace Corps Senegal Volunteers are responsible for the planning, organization, and implementation of the camp. As this is our initiative, we take great pride and care in the direction and structure of the camp. PCVs run sessions, facilitate games and activities, and operate behind the scenes to ensure smoothness and transition.</p>

<h3>The Counselors - Senegalese Instructors</h3>
<p>As with many Peace Corps projects, sustainability is a key focus. To ensure the continuity of the camp over the future, Peace Corps volunteers have invited educators from the local teachersâ€™ union to act as counselors and facilitate activities and sessions. Each year, these respected educators take on increased responsibility in order to develop their skills and encourage interest in summer camps. These essential educators play vital roles in encouraging discussion and participation.</p>
     
     
     
     
     
     
